from fastapi import APIRouter, HTTPException, Depends
from pydantic import BaseModel
from typing import List, Optional
from ..database.connection import get_db
from ..database.models import ChatSession, ChatMessage, User
from ..services.rag_chain import rag_chain_service
import uuid
import logging

logger = logging.getLogger(__name__)

router = APIRouter(prefix="/chat", tags=["chat"])


class ChatRequest(BaseModel):
    question: str
    session_id: Optional[int] = None
    file_id: Optional[int] = None


class ChatResponse(BaseModel):
    answer: str
    sources: List[str]
    timestamps: List[dict]
    session_id: int
    provider_used: Optional[str] = None


class EndSessionRequest(BaseModel):
    session_id: int


@router.post("/", response_model=ChatResponse)
async def chat(request: ChatRequest, db=Depends(get_db)):
    """
    Receives a user's question, retrieves relevant context from the vector store,
    and returns a thoughtful answer generated by the LLM.
    """
    session_id = request.session_id

    if not session_id:
        session = ChatSession(title=f"Session {uuid.uuid4().hex[:8]}")
        db.add(session)
        db.commit()
        db.refresh(session)
        session_id = session.id

    session = db.query(ChatSession).filter(
        ChatSession.id == session_id).first()
    if not session:
        raise HTTPException(
            status_code=404,
            detail="We couldn't find your chat session. Let's start a fresh one.")

    user_message = ChatMessage(
        session_id=session_id,
        role="user",
        content=request.question
    )
    db.add(user_message)
    db.commit()

    try:
        result = rag_chain_service.invoke(request.question, request.file_id)
    except RuntimeError as all_providers_failed:
        logger.error("All LLM providers exhausted: %s", all_providers_failed)
        raise HTTPException(
            status_code=503,
            detail=str(all_providers_failed)
        )
    except Exception as unexpected_error:
        logger.error("Unexpected error during inference: %s", unexpected_error)
        raise HTTPException(
            status_code=500, detail=(
                "Hmm, something unexpected happened on our end while thinking about your question. "
                "Don't worry — it's not you! Please try asking again in a moment."))

    assistant_message = ChatMessage(
        session_id=session_id,
        role="assistant",
        content=result["answer"],
        sources=str(result.get("sources", [])),
        timestamp_references=str(result.get("timestamps", []))
    )
    db.add(assistant_message)
    db.commit()

    return ChatResponse(
        answer=result["answer"],
        sources=result.get("sources", []),
        timestamps=result.get("timestamps", []),
        session_id=int(session_id),  # type: ignore
        provider_used=result.get("provider_used"),
    )


@router.post("/end-session")
async def end_session(request: EndSessionRequest, db=Depends(get_db)):
    """
    Clears the current chat session and starts a new one for the user.
    """
    session = db.query(ChatSession).filter(
        ChatSession.id == request.session_id).first()
    if not session:
        raise HTTPException(
            status_code=404,
            detail="We couldn't find your chat session. Let's start a fresh one.")

    new_session = ChatSession(title=f"Session {uuid.uuid4().hex[:8]}")
    db.add(new_session)
    db.commit()
    db.refresh(new_session)

    return {
        "status": "success",
        "message": "Session ended successfully.",
        "new_session_id": new_session.id
    }
